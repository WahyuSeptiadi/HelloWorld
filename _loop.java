// SubBab 1 Looping
class forloop{
	public static void main(String arg[]){
		for(int i=1; i<=10; i++){
			System.out.println("For loop ");
		}//System.out.println();
	}
}

// SubBab 2 Looping
class whileloop{
	public static void main(String arg[]){
		int a = 0;
		while(a < 10){
			System.out.println("While loop ");
			a++; // jangan ditaruh luar while bro
		}//System.out.println();
	}
}

